
package com.example.demo3h.model;

public class Role {
    private int roleid;
    private int uid;
    private String rolename;

    public int getRoleid() {
        return roleid;
    }

    public void setRoleid(int roleid) {
        this.roleid = roleid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getRolename() {
        return rolename;
    }
}




